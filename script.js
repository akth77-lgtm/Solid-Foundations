let revealObserver;
let counterObserver;
const LANG_STORAGE_KEY = "preferredLanguage";

function getStoredLanguage() {
  try {
    const stored = window.localStorage.getItem(LANG_STORAGE_KEY);
    return stored === "ar" || stored === "en" ? stored : null;
  } catch (error) {
    return null;
  }
}

function persistLanguage(lang) {
  try {
    window.localStorage.setItem(LANG_STORAGE_KEY, lang);
  } catch (error) {
    // Ignore storage failures (e.g., private mode restrictions).
  }
}

function handleScroll() {
  const nav = document.querySelector(".site-nav");
  const toTopButton = document.querySelector(".to-top");

  if (window.scrollY > 80) {
    if (nav) {
      nav.classList.add("is-scrolled");
    }
    if (toTopButton) {
      toTopButton.classList.add("is-visible");
    }
  } else {
    if (nav) {
      nav.classList.remove("is-scrolled");
    }
    if (toTopButton) {
      toTopButton.classList.remove("is-visible");
    }
  }
}

function initUI() {
  const elements = document.querySelectorAll("[data-i18n]");
  const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
  const langToggle = document.querySelector("[data-lang-toggle]");
  const html = document.documentElement;
  const body = document.body;
  const ltrStylesheet = document.getElementById("bootstrap-ltr");
  const rtlStylesheet = document.getElementById("bootstrap-rtl");

  const initialLang = html.getAttribute("lang") === "en" ? "en" : "ar";
  let currentLang = getStoredLanguage() || initialLang;

  function setLanguage(lang) {
    currentLang = lang;
    persistLanguage(lang);
    const dictionary = translations[lang];

    elements.forEach((element) => {
      const key = element.getAttribute("data-i18n");
      if (dictionary[key]) {
        element.textContent = dictionary[key];
      }
    });

    placeholders.forEach((element) => {
      const key = element.getAttribute("data-i18n-placeholder");
      if (dictionary[key]) {
        element.setAttribute("placeholder", dictionary[key]);
      }
    });

    if (lang === "en") {
      html.setAttribute("dir", "ltr");
      html.setAttribute("lang", "en");
      body.classList.add("is-ltr");
      ltrStylesheet.disabled = false;
      rtlStylesheet.disabled = true;
      if (langToggle) {
        langToggle.textContent = "AR";
      }
    } else {
      html.setAttribute("dir", "rtl");
      html.setAttribute("lang", "ar");
      body.classList.remove("is-ltr");
      ltrStylesheet.disabled = true;
      rtlStylesheet.disabled = false;
      if (langToggle) {
        langToggle.textContent = "EN";
      }
    }
  }

  if (langToggle && !langToggle.dataset.bound) {
    langToggle.addEventListener("click", () => {
      setLanguage(currentLang === "ar" ? "en" : "ar");
    });
    langToggle.dataset.bound = "true";
  }

  setLanguage(currentLang);

  if (!revealObserver) {
    revealObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("is-visible");
          }
        });
      },
      { threshold: 0.15 }
    );
  }

  document.querySelectorAll(".reveal").forEach((el) => {
    if (!el.dataset.revealObserved) {
      revealObserver.observe(el);
      el.dataset.revealObserved = "true";
    }
  });

  if (!counterObserver) {
    counterObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            const target = entry.target;
            const endValue = Number(target.getAttribute("data-count"));
            const valueEl = target.querySelector(".metric-value");
            let start = 0;
            const step = Math.max(1, Math.floor(endValue / 40));
            const interval = setInterval(() => {
              start += step;
              if (start >= endValue) {
                start = endValue;
                clearInterval(interval);
              }
              valueEl.textContent = start;
            }, 35);
            counterObserver.unobserve(target);
          }
        });
      },
      { threshold: 0.4 }
    );
  }

  document.querySelectorAll("[data-count]").forEach((counter) => {
    if (!counter.dataset.countObserved) {
      counterObserver.observe(counter);
      counter.dataset.countObserved = "true";
    }
  });

  if (!body.dataset.scrollBound) {
    window.addEventListener("scroll", handleScroll);
    body.dataset.scrollBound = "true";
  }

  handleScroll();

  const toTopButton = document.querySelector(".to-top");
  if (toTopButton && !toTopButton.dataset.bound) {
    toTopButton.addEventListener("click", () => {
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
    toTopButton.dataset.bound = "true";
  }

  initContactForm(currentLang);
}

function initContactForm(currentLang) {
  const form = document.querySelector(".contact-form");
  if (!form || form.dataset.bound) {
    return;
  }

  const statusEl = form.querySelector("[data-form-status]");
  const endpoint = form.getAttribute("data-contact-endpoint") || "";

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    if (!endpoint) {
      if (statusEl) {
        statusEl.textContent = translations[currentLang].form_status_error;
        statusEl.classList.add("is-error");
      }
      return;
    }

    if (statusEl) {
      statusEl.textContent = translations[currentLang].form_status_sending;
      statusEl.classList.remove("is-error", "is-success");
    }

    const payload = {
      name: form.elements.namedItem("name")?.value?.trim() || "",
      company: form.elements.namedItem("company")?.value?.trim() || "",
      email: form.elements.namedItem("email")?.value?.trim() || "",
      phone: form.elements.namedItem("phone")?.value?.trim() || "",
      message: form.elements.namedItem("message")?.value?.trim() || "",
      website: form.elements.namedItem("website")?.value || "",
    };

    try {
      const response = await fetch(endpoint, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload),
      });

      if (!response.ok) {
        throw new Error("Request failed");
      }

      form.reset();
      if (statusEl) {
        statusEl.textContent = translations[currentLang].form_status_success;
        statusEl.classList.add("is-success");
      }
    } catch (error) {
      if (statusEl) {
        statusEl.textContent = translations[currentLang].form_status_error;
        statusEl.classList.add("is-error");
      }
    }
  });

  form.dataset.bound = "true";
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initUI);
} else {
  initUI();
}

document.addEventListener("components:loaded", initUI);
